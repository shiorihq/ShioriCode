export function providerBrandIconClassName(
  provider: ProviderKind,
  fallbackClassName: string,
): string {
  switch (provider) {
    case "shiori":
      return "text-[#4F87C5] dark:text-[#7AB8E6]";
    case "cursor":
      return fallbackClassName;
    default:
      return "";
  }
}
